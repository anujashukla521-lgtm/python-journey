# Python programs demonstrating useful string methods like lower(), upper(), strip(), count(), and replace().

# Program 1 : String case conversion 
text = "I Love Python"
print(text.lower())    # Print the string to lowercase
print(text.upper())    # Print the string to uppercase

# Program 2 : Remove extra spaces
text = "   hello python    "
print(text.strip())    # Remove leading and trailing spaces

# Program 3 : Count characters
text = "hello anuja, you are a good girl"
print(text.count("a"))  # Count the number of 'a' characters

# Program 4 : Replace word in string
text = "I love Java"
print(text.replace("Java", "Python"))   # Replace 'Java' with 'Python'